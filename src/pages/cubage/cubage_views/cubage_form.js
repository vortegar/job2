// FUNCTION IMPORTS
import { addCubageFloor, deleteCubageFloor, restoreCubageFloor } from "../cubage_slices/full_cubage_sections/cubage_floor_slice";
import { getFamilyData } from "pages/families/families_services/families_service";
import { getMaterialData } from "pages/materials/materials_services/materials_service";

// AXIOS INSTANCE IMPORT
import instance from 'services/axios_config';
import { addMaterialsGroups, deleteMaterialGroups, deleteMaterialGroupsByCubicationSection, restoreMaterialGroups, updateMaterialGroups } from "../cubage_slices/full_cubage_sections/cubage_full_material_groups_slice";

// REDUX STATES
import { setConfirmOpen, setCurrent, setDeleteFloorId, setIsLoading } from "../cubage_slices/cubage_states/cubage_form_states";
import { getCubicationData, postFullCubageSections, postFullMaterialGroups } from "../cubage_services/cubage_main_form_services";
import { CircularProgress } from "../../../../node_modules/@mui/material/index";
import CubageLoader from "./cubage_loader";
import CubageConfirm from "./cubage_confirm";
import ButtonComponent from "components/main_components/button_component/index";
import { MinusCircleOutlined, ArrowLeftOutlined } from '@ant-design/icons';
import { addCopyCubageFloor, restoreCopyCubageFloor } from "../cubage_slices/full_cubage_sections/copy_floor_slice";
import { addCopyMaterialsGroups, restoreCopyMaterialGroups } from "../cubage_slices/full_cubage_sections/copy_full_groups";



function CubageForm() {
  const dispatch = useDispatch();

  // REDUX STATES
  const filters = useSelector(store => store.materialsFilters);
  // getting floor data from store
  const floor = useSelector(store => store.cubageFloor);
  const copyFloor = useSelector(store => store.copyFloor);
  const cubage = useSelector(store => store.cubageGroups);
  const copyGroups = useSelector(store => store.copyGroups);

  // redux states
  const current = useSelector(store => store.cubageFormStates.current);
  const isLoading = useSelector(store => store.cubageFormStates.isLoading);
  const confirmOpen = useSelector(store => store.cubageFormStates.confirmOpen);

  // CURRENT PROJECT STATE
  const [cubageCounter, setCubageCounter] = useState(0);
  const deleteId = '';
  const { cid, pid } = useParams();

  const reload = () => {
    window.location.reload();
  }


  // REACT-HOOK-FORM CONFIG
  const methods = useForm({
    mode: "onChange",
  });
  const {
    control,
    register,
    handleSubmit,
    getValues,
    setValue,
    errors,
    reset,
    formState
  } = methods;

  // 
  React.useEffect(() => {
    dispatch(restoreCubageFloor())
    dispatch(restoreMaterialGroups())
    //material-family data
    getFamilyData(dispatch, deleteId);
    getMaterialData(dispatch, deleteId, filters);
    //getting cubication data
    getCubicationData(dispatch, cid, deleteId);

  }, [])



  // LOADING APARTMENTS CONFIG
  const loadApartments = (data, floorNumber, lockButton) => {
    const copyNumber = data.floor[floorNumber]?.copy;
    //console.log("copyNumber", copyNumber);
    //const copyFamilies = cubage.filter(e => e.cubication_section == copyNumber);
    //console.log(copyGroups);
    const copyFamilies = copyGroups.filter(e => e.cubication_section == copyNumber);

    const array = data?.apartment_groups?.split(',');
    const { level, square_meters_surface } = data.floor[floorNumber];
    const deptos = []
    const deleteId = '';
    array.map((depto) => {
      deptos.push({ ctos: [1], apartment_number: depto, apartment_type: '' });
    })
    // UPLOAD DATA TO API
    postFullCubageSections(dispatch, cid, level, square_meters_surface, deptos, deleteId, copyNumber, copyFamilies, lockButton);
  }


  // SUBMIT CUBAGE FAMILY DATA
  const submitFloor = (data, floorNumber, floorIndex, index) => {
    const cubication_section = floor[floorNumber].cubication_section;
    const material_family = parseInt(data?.floor[floorIndex]?.families[index]?.material_family);
    const materials = data?.floor[floorIndex]?.families[index]?.materials;
    postFullMaterialGroups(dispatch, cubication_section, material_family, materials, deleteId);



    ///////////////////PUT de PISO

    const level = data.floor[floorNumber].level;
    const square_meters_surface = data.floor[0].square_meters_surface;
    const deptos = data.floor[floorNumber].apartment;

    // METODO PARA CAMBIAR EL TIPO DE DEPARTAMENTO NO BORRAR!!!!!!!!!!!!
    //instance.put(`full_cubage_sections/${cubication_section}/`, {
    //  cubication: cid,
    //  level: level,
    //  square_meters_surface: square_meters_surface,
    //  apartment_groups: deptos,
    //}).then((response) => {
    //  toast.success("Piso Actualizado correctamente");
    //}).catch((error) => {
    //  toast.error("Error al ACTUALIZAR0 Piso");
    //})
  };

  const updateFloorData = (data, floorNumber) => {
    const cubication_section = floor[floorNumber].cubication_section;


    const level = data.floor[floorNumber].level;
    const square_meters_surface = data.floor[0].square_meters_surface;
    const deptos = data.floor[floorNumber].apartment;

    // METODO PARA CAMBIAR EL TIPO DE DEPARTAMENTO!!!!!!!!!!!!
    instance.put(`full_cubage_sections/${cubication_section}/`, {
      cubication: cid,
      level: level,
      square_meters_surface: square_meters_surface,
      apartment_groups: deptos,
    }).then((response) => {
      toast.success("Piso Actualizado correctamente");
    }).catch((error) => {
      toast.error("Error al ACTUALIZAR Piso");
    })
  }



  const putFloor = (data, floorNumber, floorIndex, index) => {
    const cubication_section = floor[floorNumber].cubication_section;
    const deleteId = "";
    const modId = data?.floor[floorNumber]?.families[index]?.familyId;
    instance.put(`full_material_groups/${modId}/`, {   // FALTA SETEAR EL INDEX POR EL CUAL SE SACA EL ID, probablemente sea la variable index
      cubication_section: cubication_section,
      material_family: parseInt(data?.floor[floorIndex]?.families[index]?.material_family),
      materials: data?.floor[floorIndex]?.families[index]?.materials,
    }).then((response) => {
      const materias = response?.data?.materials;
      const material_family = response?.data?.material_family;
      const id = response?.data?.id;
      toast.success("Familia Actualizada")
      dispatch(addMaterialsGroups({ id: id, material_family: material_family, materias: materias }))
      dispatch(updateMaterialGroups({ id: id, materias: materias }))
      dispatch(deleteMaterialGroups(deleteId));
      reset();
      /* window.location.reload(); */
    }).catch((error) => {
      toast.error("Error al cargar familia")
    })
  };

  const deleteFamily = (data, floorNumber, floorIndex, index, idDelete) => {
    const idFamily = data?.floor[floorNumber].families[index].familyId;
    const cubication_section = floor[floorNumber].cubication_section;
    instance.delete(`full_material_groups/${idFamily}/`)
      .then((response) => {
        //console.log(response);
        toast.success("Familia eliminada correctamente")
        /* reload(); */

      }).catch((error) => {
        console.error(error);
      })
  }

  const deleteFloor = (data, floorNumber) => {
    const cubication_section = floor[floorNumber]?.cubication_section;
    const deleteId = "";




    const idFloor = data?.floor[floorNumber]?.floorId;


    const deleteFl = copyFloor[floorNumber + 1]?.cubication_section;


    dispatch(setDeleteFloorId(deleteFl));
    dispatch(setConfirmOpen(true));

  }

  // SETTING PREVIOUS DATA OF PROJECT
  setValue("project", current?.data_sheet?.project_title);

  const cargarDatos = () => {
    setCubageCounter(cubageCounter + 1);
    const deleteId = "";
    instance.get(`full_cubage_sections/?limit=100&cubication=${cid}`)
      .then((response) => {
        const cubicaciones = response?.data?.results;
        const cubi = response?.data?.results[cubageCounter];
        const cid = cubi.cubication;
        const deptosRes = cubi.apartment_groups;
        const level = cubi.level;
        const square_meters_surface = cubi.square_meters_surface;
        const cubication_section = cubi.id;
        dispatch(deleteCubageFloor(deleteId));
        setTimeout(() => {
          dispatch(setIsLoading(false));
        }, 5000)
        cubicaciones.map((item, index) => {
          const cid = item.cubication;
          const deptosRes = item.apartment_groups;
          const level = item.level;
          const square_meters_surface = item.square_meters_surface;
          const cubication_section = item.id;
          dispatch(addCubageFloor({ id: cid, apartment_groups: deptosRes, level: level, square_meters_surface: square_meters_surface, cubication_section: cubication_section, isNew: false }))
          dispatch(addCopyCubageFloor({ id: cid, apartment_groups: deptosRes, level: level, square_meters_surface: square_meters_surface, cubication_section: cubication_section, isNew: false }))
          dispatch(deleteCubageFloor(deleteId));
          instance.get(`full_material_groups/?limit=1000&cubication_section=${item.id}`)
            .then((response) => {
              const datos = response?.data?.results;
              datos.map((datt) => {
                const materias = datt.materials;
                const cubication_section = datt.cubication_section;
                const material_family = datt.material_family;
                const id = datt.id;
                const isNew = true;
                dispatch(addMaterialsGroups({ id: id, material_family: material_family, cubication_section: cubication_section, materias: materias, isNew: isNew, isRemove: false}))
                dispatch(addCopyMaterialsGroups({ id: id, material_family: material_family, cubication_section: cubication_section, materias: materias, isNew: isNew, isRemove: false }))
                dispatch(deleteMaterialGroups(deleteId));
                setTimeout(() => {
                  dispatch(setIsLoading(false));
                }, 5000)
              })
            })
            .catch((error) => {
              console.error(error)
              setTimeout(() => {
                dispatch(setIsLoading(false));
              }, 5000)

            })
        })
      })
      .catch((error) => {
        console.error(error);
        dispatch(setIsLoading(false));
      })
  }

  React.useEffect(() => {
    dispatch(restoreCubageFloor());
    dispatch(restoreMaterialGroups());
    dispatch(restoreCopyCubageFloor());
    dispatch(restoreCopyMaterialGroups());
    setIsLoading(true);
    cargarDatos();
  }, [])


  const closeConfirm = () => {
    dispatch(setConfirmOpen(false));
  }
  const restoreLoading = () => {
    dispatch(setIsLoading(true))
  }



  return (
    <FormProvider {...methods}>
      <CubageLoader />
      <CubageConfirm open={confirmOpen} /* reload={reload} */ close={closeConfirm} />

      <Box sx={{mb: 1}}>
      <Link to={`/cubage/${pid}`}>
                <ButtonComponent variant="contained" color='warning' tooltip="Listado de Actividades" onClick={restoreLoading}>
                  <ArrowLeftOutlined />&nbsp;&nbsp;Volver
                </ButtonComponent>
              </Link></Box>


      <MainCard sx={{ height: 'none' }}>
        <ToastContainer />
        <form onSubmit={handleSubmit()}>
          <Box
            sx={{
              display: 'flex',
              justifyContent: 'space-between',
              p: 1,
              m: 1,
              bgcolor: 'background.paper',
              borderRadius: 1,
            }}
          >

            <Box>
              <TitleComponent>Cubicación: {current?.activity}</TitleComponent>
              </Box>
            <Box><TitleComponent>Projecto: {current?.data_sheet?.project_title}</TitleComponent></Box>
          </Box>
          <Box sx={{ width: '100%', marginTop: '40px' }}>
            <TableContainer component={Paper}>
              <Table sx={{ boeder: 3, '$ .MuiTableRow-root': { height: 2000 }, '$ .MuiTableBody-root': { border: 3, borderRadius: '14px' } }} >
                <TableBody>
                  <FloorArray
                    {...{ control, register, getValues, errors, handleSubmit, submitFloor, setValue, cargarDatos, putFloor, loadApartments, reset, deleteFamily, deleteFloor, updateFloorData }}
                  />
                </TableBody>
              </Table>
            </TableContainer>
          </Box>
        </form>
      </MainCard>
    </FormProvider>
  );
}

export default CubageForm;



