// REACT IMPORTS
import { createSlice } from '@reduxjs/toolkit';

// DEMO DATA INITIAL STATE
const initialState = [];

// PRODUCTS SLICE AND REDUCERS CONFIGURATIONS
const materialSlice = createSlice({
    name: 'materials',
    initialState,
    reducers: {
        addMaterial: (state, action) => {
            const { id } = action.payload;
            const existingMaterial = state.find(material => material.id === id);
            if (!existingMaterial) {
                state.push(action.payload);
            }
        },
        editMaterial: (state, action) => {
            const {
                id,
                name,
                description,
                code,
                price,
                measure_unit,
                material_family,
                material_family_id,
                material_subfamily,
                material_subfamily_id,
            } = action.payload;
            const existingMaterial = state.find(material => material.id === id);
            if (existingMaterial) {
                existingMaterial.name = name;
                existingMaterial.description = description;
                existingMaterial.code = code;
                existingMaterial.price = price;
                existingMaterial.measure_unit = measure_unit;
                existingMaterial.material_family = material_family;
                existingMaterial.material_family_id = material_family_id;
                existingMaterial.material_subfamily = material_subfamily;
                existingMaterial.material_subfamily_id = material_subfamily_id;
            }
        },
        deleteMaterial: (state, action) => {
            const { id } = action.payload;
            const existingMaterial = state.find(material => material.id === id);
            if (existingMaterial) {
                return state.filter(material => material.id !== id);
            }
        },
        restoreMaterial: (state, action) => {
            const initialState = [
                {
                    id: '',
                    name: '',
                    description: '',
                    code: '',
                    price: '',
                    measure_unit: '',
                    material_family: '',
                    material_subfamily: null,
                }
            ];
            return state= initialState;
        }
    }
});

export const { addMaterial, editMaterial, deleteMaterial, restoreMaterial } = materialSlice.actions;
export default materialSlice.reducer;
